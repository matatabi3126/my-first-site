(function () {
  var STORAGE_KEY = 'lang';
  var DEFAULT_LANG = 'ja';

  function getLang() {
    return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG;
  }

  function applyLanguage(lang) {
    var dict = window.TRANSLATIONS[lang] || window.TRANSLATIONS[DEFAULT_LANG];
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (dict[key] != null) el.textContent = dict[key];
    });

    var attrTargets = ['aria-label', 'placeholder', 'alt', 'title', 'content', 'href'];
    attrTargets.forEach(function (attr) {
      document.querySelectorAll('[data-i18n-' + attr + ']').forEach(function (el) {
        var key = el.getAttribute('data-i18n-' + attr);
        if (dict[key] != null) el.setAttribute(attr, dict[key]);
      });
    });

    document.querySelectorAll('.lang-option').forEach(function (btn) {
      btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
    });

    document.dispatchEvent(new CustomEvent('languagechange:caccio', { detail: { lang: lang } }));
  }

  function setLang(lang) {
    localStorage.setItem(STORAGE_KEY, lang);
    applyLanguage(lang);
  }

  window.CACCIO_I18N = { getLang: getLang, setLang: setLang, translate: function (key) {
    var dict = window.TRANSLATIONS[getLang()] || window.TRANSLATIONS[DEFAULT_LANG];
    return dict[key];
  } };

  document.addEventListener('DOMContentLoaded', function () {
    applyLanguage(getLang());

    document.querySelectorAll('.lang-option').forEach(function (btn) {
      btn.addEventListener('click', function () {
        setLang(btn.dataset.lang);
      });
    });
  });
})();
